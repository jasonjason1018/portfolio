"use strict";
function answer01(){
		var q1 = document.getElementById("q1-2").checked;
		var q2 = document.getElementById("q2-1").checked;
		var q3 = document.getElementById("q3-3").checked;
		if(q1==true && q2 == true&& q3 == true){
			document.getElementById("yes").style.display="";
			document.getElementById("no").style.display="none";
		}else{
			document.getElementById("yes").style.display="none";
			document.getElementById("no").style.display="";
		}
		
	}

